def maxArea(height):
    left, right = 0, len(height) - 1
    max_area = 0
    while left < right:
        width = right - left
        max_area = max(max_area, min(height[left], height[right]) * width)
        if height[left] < height[right]:
            left += 1
        else:
            right -= 1
    return max_area

# Example inputs
inputs = [
    [1, 8, 6, 2, 5, 4, 8, 3, 7],
    [1, 1],
    [4, 3, 2, 1, 4]
]
# Expected outputs
outputs = [49, 1, 16]

# Verification
for i in range(len(inputs)):
    result = maxArea(inputs[i])
    print(result == outputs[i])